package com.example.tetris;

class Coordenada {
    private int valX;
    private int valY;

    public Coordenada(int valX, int valY) {
        this.valX = valX;
        this.valY = valY;
    }

    public int getValX() {
        return valX;
    }

    public void setValX(int valX) {
        this.valX = valX;
    }

    public int getValY() {
        return valY;
    }

    public void setValY(int valY) {
        this.valY = valY;
    }
}
